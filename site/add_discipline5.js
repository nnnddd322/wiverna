function formatDiscipline(discipline) {
  let result = '  {\n';
  result += `    id: '${discipline.id}',\n`;
  result += `    name: '${discipline.name}',\n`;
  result += `    description: '${discipline.description}',\n`;
  
  // PDF Ñ„Ğ°Ğ¹Ğ»Ñ‹
  result += '    pdfFiles: [\n';
  for (const pdf of discipline.pdfFiles) {
    result += '      {\n';
    result += `        title: '${pdf.title}',\n`;
    result += `        path: '${pdf.path}',\n`;
    result += `        fileName: '${pdf.fileName}'\n`;
    result += '      }\n';
  }
  result += '    ],\n';
  
  // ĞŸÑ€ĞµĞ·ĞµĞ½Ñ‚Ğ°Ñ†Ğ¸Ğ¸
  result += '    presentations: [\n';
  for (const pres of discipline.presentations) {
    result += '      {\n';
    result += `        title: '${pres.title}',\n`;
    result += '        slides: [\n';
    
    for (const slide of pres.slides) {
      result += '          {\n';
      result += `            type: '${slide.type}',\n`;
      
      if (slide.title) result += `            title: '${slide.title.replace(/'/g, "\\'")}',\n`;
      if (slide.subtitle) result += `            subtitle: '${slide.subtitle.replace(/'/g, "\\'")}',\n`;
      if (slide.highlightText) result += `            highlightText: '${slide.highlightText.replace(/'/g, "\\'")}',\n`;
      
      if (slide.content) {
        result += '            content: [\n';
        for (const item of slide.content) {
          result += `              '${item.replace(/'/g, "\\'")}',\n`;
        }
        result += '            ]\n';
      }
      
      if (slide.leftContent) {
        result += '            leftContent: [\n';
        for (const item of slide.leftContent) {
          result += `              '${item.replace(/'/g, "\\'")}',\n`;
        }
        result += '            ],\n';
      }
      
      if (slide.rightContent) {
        result += '            rightContent: [\n';
        for (const item of slide.rightContent) {
          result += `              '${item.replace(/'/g, "\\'")}',\n`;
        }
        result += '            ]\n';
      }
      
      if (slide.leftColumn) {
        result += '            leftColumn: {\n';
        result += `              title: '${slide.leftColumn.title.replace(/'/g, "\\'")}',\n`;
        result += '              items: [\n';
        for (const item of slide.leftColumn.items) {
          result += `                '${item.replace(/'/g, "\\'")}',\n`;
        }
        result += '              ]\n';
        result += '            },\n';
      }
      
      if (slide.rightColumn) {
        result += '            rightColumn: {\n';
        result += `              title: '${slide.rightColumn.title.replace(/'/g, "\\'")}',\n`;
        result += '              items: [\n';
        for (const item of slide.rightColumn.items) {
          result += `                '${item.replace(/'/g, "\\'")}',\n`;
        }
        result += '              ]\n';
        result += '            }\n';
      }
      
      if (slide.gridItems) {
        result += '            gridItems: [\n';
        for (const item of slide.gridItems) {
          result += '              {\n';
          result += `                icon: '${item.icon}',\n`;
          result += `                title: '${item.title.replace(/'/g, "\\'")}',\n`;
          result += `                text: '${item.text.replace(/'/g, "\\'")}'\n`;
          result += '              },\n';
        }
        result += '            ]\n';
      }
      
      result += '          },\n';
    }
    
    result += '        ]\n';
    result += '      },\n';
  }
  result += '    ]\n';
  result += '  },\n';
  
  return result;
}

